def get_snakes():
    snakes = {}
    for entry_point in pkg_resources.iter_entry_points('snake_types'):
        snakes[entry_point.name] = entry_point.load()
    return snakes


def main():
    snake_type = args.type or 'normal'
    print(get_snakes()[snake_type])

if __name__ == '__main__':
    main()
